package com.graduation.panda.model;

public class Apply {
    private int id;
    private String applyName;
    private String applyEmail;
    private String applyPhone;
    private String applyKind;
    private String applyGoodsname;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getApplyName() {
        return applyName;
    }

    public void setApplyName(String applyName) {
        this.applyName = applyName;
    }

    public String getApplyEmail() {
        return applyEmail;
    }

    public void setApplyEmail(String applyEmail) {
        this.applyEmail = applyEmail;
    }

    public String getApplyPhone() {
        return applyPhone;
    }

    public void setApplyPhone(String applyPhone) {
        this.applyPhone = applyPhone;
    }

    public String getApplyKind() {
        return applyKind;
    }

    public void setApplyKind(String applyKind) {
        this.applyKind = applyKind;
    }

    public String getApplyGoodsname() {
        return applyGoodsname;
    }

    public void setApplyGoodsname(String applyGoodsname) {
        this.applyGoodsname = applyGoodsname;
    }


}
