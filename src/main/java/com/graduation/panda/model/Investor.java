package com.graduation.panda.model;

public class Investor {
    private int id;
    private String investorName;
    private String investorEmail;
    private String investorPhone;
    private String investorAim;
    private String investorMoney;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getInvestorName() {
        return investorName;
    }

    public void setInvestorName(String investorName) {
        this.investorName = investorName;
    }

    public String getInvestorEmail() {
        return investorEmail;
    }

    public void setInvestorEmail(String investorEmail) {
        this.investorEmail = investorEmail;
    }

    public String getInvestorPhone() {
        return investorPhone;
    }

    public void setInvestorPhone(String investorPhone) {
        this.investorPhone = investorPhone;
    }

    public String getInvestorAim() {
        return investorAim;
    }

    public void setInvestorAim(String investorAim) {
        this.investorAim = investorAim;
    }

    public String getInvestorMoney() {
        return investorMoney;
    }

    public void setInvestorMoney(String investorMoney) {
        this.investorMoney = investorMoney;
    }

}
